# 3개의 정수(a, b, c)가 입력되었을 때, 짝(even)/홀(odd)을 출력해보자.

# 예시
# ...
# if a%2==0 :
#   print("even")
# else :
#   print("odd") 
# ...

a, b, c = input().split()

a = int(a)
b = int(b)
c = int(c)

if a%2==0 :
  print("even")
else :
  print("odd") 

if b%2==0 :
  print("even")
else :
  print("odd") 

if c%2==0 :
  print("even")
else :
  print("odd") 
